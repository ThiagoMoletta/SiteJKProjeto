
package br.com.DTO;

public class OrcamentoDTO {
    private int id_orc;
    private String nome;
    private String num;
    private String email;
    private String msg;

    public int getId_orc() {
        return id_orc;
    }

    public void setId_orc(int id_orc) {
        this.id_orc = id_orc;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
    
    
    
}
